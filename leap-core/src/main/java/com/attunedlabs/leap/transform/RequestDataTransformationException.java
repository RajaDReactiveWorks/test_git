package com.attunedlabs.leap.transform;

public class RequestDataTransformationException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1265668620496985842L;

	public RequestDataTransformationException() {
		super();
	}

	public RequestDataTransformationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public RequestDataTransformationException(String message, Throwable cause) {
		super(message, cause);
	}

	public RequestDataTransformationException(String message) {
		super(message);
	}

	public RequestDataTransformationException(Throwable cause) {
		super(cause);
	}
	
	

}
