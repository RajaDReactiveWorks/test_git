package com.attunedlabs.leap.feature.routing;

public class DynamicallyImplRoutingFailedException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 2436644130352641841L;

	public DynamicallyImplRoutingFailedException() {
		super();
	}

	public DynamicallyImplRoutingFailedException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DynamicallyImplRoutingFailedException(String message, Throwable cause) {
		super(message, cause);
	}

	public DynamicallyImplRoutingFailedException(String message) {
		super(message);
	}

	public DynamicallyImplRoutingFailedException(Throwable cause) {
		super(cause);
	}
	
	

}
