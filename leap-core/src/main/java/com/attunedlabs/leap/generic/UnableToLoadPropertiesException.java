package com.attunedlabs.leap.generic;

public class UnableToLoadPropertiesException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7789488071008708853L;
	
	
	public UnableToLoadPropertiesException() {
		super();
	}

	public UnableToLoadPropertiesException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UnableToLoadPropertiesException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnableToLoadPropertiesException(String message) {
		super(message);
	}

	public UnableToLoadPropertiesException(Throwable cause) {
		super(cause);
	}
	
	

}
