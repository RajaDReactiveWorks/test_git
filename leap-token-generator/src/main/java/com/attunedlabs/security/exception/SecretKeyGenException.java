package com.attunedlabs.security.exception;

public class SecretKeyGenException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7390043880518420503L;

	public SecretKeyGenException() {
	}

	public SecretKeyGenException(String arg0) {
		super(arg0);
	}

	public SecretKeyGenException(Throwable arg0) {
		super(arg0);
	}

	public SecretKeyGenException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SecretKeyGenException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
