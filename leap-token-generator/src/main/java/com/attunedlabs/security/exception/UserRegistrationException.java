package com.attunedlabs.security.exception;

public class UserRegistrationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1247496089591757976L;

	public UserRegistrationException() {
	}

	public UserRegistrationException(String arg0) {
		super(arg0);
	}

	public UserRegistrationException(Throwable arg0) {
		super(arg0);
	}

	public UserRegistrationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UserRegistrationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
