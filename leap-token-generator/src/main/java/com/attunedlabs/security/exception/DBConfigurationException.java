package com.attunedlabs.security.exception;

public class DBConfigurationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5152267489803970150L;

	public DBConfigurationException() {
	}

	public DBConfigurationException(String arg0) {
		super(arg0);
	}

	public DBConfigurationException(Throwable arg0) {
		super(arg0);
	}

	public DBConfigurationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DBConfigurationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
