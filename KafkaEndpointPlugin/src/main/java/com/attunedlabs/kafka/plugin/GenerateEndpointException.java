package com.attunedlabs.kafka.plugin;

public class GenerateEndpointException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -185702467647668582L;

	public GenerateEndpointException() {
		super();
	}

	public GenerateEndpointException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public GenerateEndpointException(String message, Throwable cause) {
		super(message, cause);
	}

	public GenerateEndpointException(String message) {
		super(message);
	}

	public GenerateEndpointException(Throwable cause) {
		super(cause);
	}
	
	

}
