package com.attunedlabs.featureinstaller.util;

public class FeatureMetaInfoResourceException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 5617182206605845210L;

	public FeatureMetaInfoResourceException() {
		super();
	}

	public FeatureMetaInfoResourceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FeatureMetaInfoResourceException(String message, Throwable cause) {
		super(message, cause);
	}

	public FeatureMetaInfoResourceException(String message) {
		super(message);
	}

	public FeatureMetaInfoResourceException(Throwable cause) {
		super(cause);
	}
	
	

}
