package com.attunedlabs.featureInstaller.bundle.tracker.impl;

public class FeatureMetaInfoExtenderException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2720926402332493555L;
	
	public FeatureMetaInfoExtenderException() {
		super();
	}

	public FeatureMetaInfoExtenderException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public FeatureMetaInfoExtenderException(String message, Throwable cause) {
		super(message, cause);
	}

	public FeatureMetaInfoExtenderException(String message) {
		super(message);
		
	}

	public FeatureMetaInfoExtenderException(Throwable cause) {
		super(cause);
	}

}
