package com.attunedlabs.integrationfwk.jdbcIntactivity.config.helper;

public class JdbcIntActivityStringParserException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JdbcIntActivityStringParserException() {
		super();
	}

	public JdbcIntActivityStringParserException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public JdbcIntActivityStringParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public JdbcIntActivityStringParserException(String message) {
		super(message);
	}

	public JdbcIntActivityStringParserException(Throwable cause) {
		super(cause);
	}

}
