package com.attunedlabs.integrationfwk.jdbcIntactivity.config;

public class JdbcIntActivityConfigurationLoaderException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JdbcIntActivityConfigurationLoaderException() {
		super();
	}

	public JdbcIntActivityConfigurationLoaderException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public JdbcIntActivityConfigurationLoaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public JdbcIntActivityConfigurationLoaderException(String message) {
		super(message);
	}

	public JdbcIntActivityConfigurationLoaderException(Throwable cause) {
		super(cause);
	}

}
