package com.attunedlabs.integrationfwk.config.impl;

public class IntegrationPipelineConfigurationServiceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public IntegrationPipelineConfigurationServiceException() {
		super();
	}

	public IntegrationPipelineConfigurationServiceException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public IntegrationPipelineConfigurationServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public IntegrationPipelineConfigurationServiceException(String message) {
		super(message);
	}

	public IntegrationPipelineConfigurationServiceException(Throwable cause) {
		super(cause);
	}

}
