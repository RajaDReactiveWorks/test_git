package com.attunedlabs.integrationfwk.config;

public class IntegrationPipelineConfigParserException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public IntegrationPipelineConfigParserException() {
		super();
	}

	public IntegrationPipelineConfigParserException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public IntegrationPipelineConfigParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public IntegrationPipelineConfigParserException(String message) {
		super(message);
	}

	public IntegrationPipelineConfigParserException(Throwable cause) {
		super(cause);
	}

}
