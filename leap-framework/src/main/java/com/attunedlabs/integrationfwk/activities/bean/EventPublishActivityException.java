package com.attunedlabs.integrationfwk.activities.bean;

public class EventPublishActivityException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 733722209431516139L;

	public EventPublishActivityException() {
		super();
	}

	public EventPublishActivityException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public EventPublishActivityException(String message, Throwable cause) {
		super(message, cause);
	}

	public EventPublishActivityException(String message) {
		super(message);
	}

	public EventPublishActivityException(Throwable cause) {
		super(cause);
	}
	
	

}
