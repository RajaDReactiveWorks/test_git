package com.attunedlabs.staticconfig;

public class StaticConfigDeleteException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -417591793050373611L;

	public StaticConfigDeleteException() {
		super();
	}

	public StaticConfigDeleteException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public StaticConfigDeleteException(String message, Throwable cause) {
		super(message, cause);
	}

	public StaticConfigDeleteException(String message) {
		super(message);
	}

	public StaticConfigDeleteException(Throwable cause) {
		super(cause);
	}

}
