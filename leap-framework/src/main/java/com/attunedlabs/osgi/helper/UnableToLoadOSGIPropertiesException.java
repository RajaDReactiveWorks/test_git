package com.attunedlabs.osgi.helper;

public class UnableToLoadOSGIPropertiesException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3187115859215101771L;

	public UnableToLoadOSGIPropertiesException() {
		super();
	}

	public UnableToLoadOSGIPropertiesException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UnableToLoadOSGIPropertiesException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnableToLoadOSGIPropertiesException(String message) {
		super(message);
	}

	public UnableToLoadOSGIPropertiesException(Throwable cause) {
		super(cause);
	}
	
	

}
