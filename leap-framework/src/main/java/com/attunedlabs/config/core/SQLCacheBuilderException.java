package com.attunedlabs.config.core;

public class SQLCacheBuilderException extends Exception {

	public SQLCacheBuilderException() {
		super();
		
	}

	public SQLCacheBuilderException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public SQLCacheBuilderException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public SQLCacheBuilderException(String message) {
		super(message);
		
	}

	public SQLCacheBuilderException(Throwable cause) {
		super(cause);
		
	}

}
