package com.attunedlabs.permastore.config;

public class PermaStoreConfigurationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1706215457892993874L;

	public PermaStoreConfigurationException() {
		super();
	}

	public PermaStoreConfigurationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public PermaStoreConfigurationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PermaStoreConfigurationException(String arg0) {
		super(arg0);
	}

	public PermaStoreConfigurationException(Throwable arg0) {
		super(arg0);
	}

}
