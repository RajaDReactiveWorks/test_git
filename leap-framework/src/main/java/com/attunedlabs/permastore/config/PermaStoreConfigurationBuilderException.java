package com.attunedlabs.permastore.config;

public class PermaStoreConfigurationBuilderException extends PermaStoreConfigurationException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 6174657076636834237L;

	public PermaStoreConfigurationBuilderException() {
		super();
	}

	public PermaStoreConfigurationBuilderException(String msg, Throwable arg1, boolean arg2, boolean arg3) {
		super(msg, arg1, arg2, arg3);
	}

	public PermaStoreConfigurationBuilderException(String msg, Throwable throwable) {
		super(msg, throwable);
	}

	public PermaStoreConfigurationBuilderException(String msg) {
		super(msg);
	}

	public PermaStoreConfigurationBuilderException(Throwable throwable) {
		super(throwable);
	}

}
