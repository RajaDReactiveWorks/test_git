package com.attunedlabs.eventframework.abstractbean.util;

public class ConnectionConfigurationException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 2505029066803425492L;

	public ConnectionConfigurationException() {
		super();
	}

	public ConnectionConfigurationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ConnectionConfigurationException(String message, Throwable cause) {
		super(message, cause);
	}

	public ConnectionConfigurationException(String message) {
		super(message);
	}

	public ConnectionConfigurationException(Throwable cause) {
		super(cause);
	}
	
	

}
