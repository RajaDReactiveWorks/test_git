package com.attunedlabs.eventframework.camel;

public class EventBuilderInstantiationException extends Exception{

	public EventBuilderInstantiationException() {
		super();
		
	}

	public EventBuilderInstantiationException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public EventBuilderInstantiationException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public EventBuilderInstantiationException(String message) {
		super(message);
	
	}

	public EventBuilderInstantiationException(Throwable cause) {
		super(cause);
		
	}

}
