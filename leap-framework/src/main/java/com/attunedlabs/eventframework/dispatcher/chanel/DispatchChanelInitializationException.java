package com.attunedlabs.eventframework.dispatcher.chanel;

public class DispatchChanelInitializationException extends Exception {
	private static final long serialVersionUID = 7583863823729821247L;

	public DispatchChanelInitializationException() {
	}

	public DispatchChanelInitializationException(String message) {
		super(message);
	}

	public DispatchChanelInitializationException(Throwable cause) {
		super(cause);
	}

	public DispatchChanelInitializationException(String message, Throwable cause) {
		super(message, cause);
	}

	public DispatchChanelInitializationException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
