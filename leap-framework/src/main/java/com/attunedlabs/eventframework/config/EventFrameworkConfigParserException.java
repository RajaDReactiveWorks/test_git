package com.attunedlabs.eventframework.config;

public class EventFrameworkConfigParserException extends Exception {
	private static final long serialVersionUID = -3874472908905801102L;

	public EventFrameworkConfigParserException() {
		super();
	}

	public EventFrameworkConfigParserException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public EventFrameworkConfigParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public EventFrameworkConfigParserException(String message) {
		super(message);
	}

	public EventFrameworkConfigParserException(Throwable cause) {
		super(cause);
	}

}
