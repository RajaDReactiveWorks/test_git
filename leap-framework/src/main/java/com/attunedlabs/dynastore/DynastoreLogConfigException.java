package com.attunedlabs.dynastore;

public class DynastoreLogConfigException extends Exception{
	/**
	 * 
	 */
	private static final long serialVersionUID = 3174162226831183189L;
	
	public DynastoreLogConfigException() {
		super();
	}

	public DynastoreLogConfigException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DynastoreLogConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public DynastoreLogConfigException(String message) {
		super(message);
	}

	public DynastoreLogConfigException(Throwable cause) {
		super(cause);
	}

}
