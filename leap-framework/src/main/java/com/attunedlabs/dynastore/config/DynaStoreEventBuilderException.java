package com.attunedlabs.dynastore.config;

public class DynaStoreEventBuilderException extends Exception {
	private static final long serialVersionUID = 2330930620791873167L;

	public DynaStoreEventBuilderException() {
		super();
	}

	public DynaStoreEventBuilderException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DynaStoreEventBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

	public DynaStoreEventBuilderException(String message) {
		super(message);
	}

	public DynaStoreEventBuilderException(Throwable cause) {
		super(cause);
	}

}
