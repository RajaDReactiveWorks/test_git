package com.attunedlabs.datacontext.config;

public class DataContextConfigurationException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 7082336868659823706L;

	public DataContextConfigurationException() {
		super();
	}

	public DataContextConfigurationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DataContextConfigurationException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataContextConfigurationException(String message) {
		super(message);
	}

	public DataContextConfigurationException(Throwable cause) {
		super(cause);
	}
	
	

}
