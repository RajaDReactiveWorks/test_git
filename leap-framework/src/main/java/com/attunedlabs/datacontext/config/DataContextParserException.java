package com.attunedlabs.datacontext.config;

public class DataContextParserException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1658889976228301068L;

	public DataContextParserException() {
		super();
	}

	public DataContextParserException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DataContextParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataContextParserException(String message) {
		super(message);
	}

	public DataContextParserException(Throwable cause) {
		super(cause);
	}
	
	

}
