package com.attunedlabs.policy.config;

public class PolicyConfigurationException extends Exception {

	public PolicyConfigurationException() {
		super();
	}

	public PolicyConfigurationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public PolicyConfigurationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PolicyConfigurationException(String arg0) {
		super(arg0);
	}

	public PolicyConfigurationException(Throwable arg0) {
		super(arg0);
	}

}
