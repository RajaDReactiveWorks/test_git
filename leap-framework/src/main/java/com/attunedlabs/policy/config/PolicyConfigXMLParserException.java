package com.attunedlabs.policy.config;

public class PolicyConfigXMLParserException extends Exception {

	public PolicyConfigXMLParserException() {
		super();
		
	}

	public PolicyConfigXMLParserException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public PolicyConfigXMLParserException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public PolicyConfigXMLParserException(String message) {
		super(message);
		
	}

	public PolicyConfigXMLParserException(Throwable cause) {
		super(cause);
		
	}

}
