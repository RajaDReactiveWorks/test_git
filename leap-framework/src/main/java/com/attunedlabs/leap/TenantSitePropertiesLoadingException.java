package com.attunedlabs.leap;

public class TenantSitePropertiesLoadingException extends Exception{

	private static final long serialVersionUID = -6797055095945743595L;

	public TenantSitePropertiesLoadingException() {
		super();
	}

	public TenantSitePropertiesLoadingException(String message,
			Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TenantSitePropertiesLoadingException(String message, Throwable cause) {
		super(message, cause);
	}

	public TenantSitePropertiesLoadingException(String message) {
		super(message);
	}

	public TenantSitePropertiesLoadingException(Throwable cause) {
		super(cause);
	}
	
	

}
