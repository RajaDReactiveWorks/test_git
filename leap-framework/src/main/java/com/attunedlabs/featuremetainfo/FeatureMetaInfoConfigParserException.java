package com.attunedlabs.featuremetainfo;

public class FeatureMetaInfoConfigParserException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6648621123202352178L;
	
	public FeatureMetaInfoConfigParserException() {
		super();
	}

	public FeatureMetaInfoConfigParserException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public FeatureMetaInfoConfigParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public FeatureMetaInfoConfigParserException(String message) {
		super(message);
		
	}

	public FeatureMetaInfoConfigParserException(Throwable cause) {
		super(cause);
	}

}
